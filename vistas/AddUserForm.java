    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String nombre = txtName.getText();
        String apellidos = txtLastName.getText();
        String tipoDocumento = (String)cbDocumentType.getSelectedItem();
        String documento = txtDocument.getText();
        String correo = txtImail.getText();
        System.out.println("nombre:" + nombre + "apellidos:" + apellidos + "tipo Documento:" + tipoDocumento + "documento:" + documento + "correo:" + correo );
        //validamos
        if(nombre.isEmpty() ||  apellidos.isEmpty() || documento.isEmpty() ||  correo.isEmpty()){
        JOptionPane.showMessageDialog(this,"hay campos sin diligencir", "Formulario Registro ", JOptionPane.WARNING_MESSAGE);
        }else {
            this.dispose();
            String query = " INSERT INTO `empleados`(`nombre`, `apellidos`, `tipoDocumento`, `documento`, `correo`) VALUES ('"+ nombre +"','"+ apellidos +"','"+ 
                    tipoDocumento +"','"+ documento +"','"+ correo +"')";
            try {
                connection = Conexion.getConnection();
                st = connection.createStatement();
                st.executeUpdate(query);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "No se pudo crear el usuario", "Registro", JOptionPane.ERROR_MESSAGE);
            }
                        
            JOptionPane.showMessageDialog(this, "REGISTRO EXITOSO");
             System.out.println("nombre:" + nombre + "apellidos:" + apellidos + "tipo Documento:" + tipoDocumento + "documento:" + documento + "correo:" + correo );
        }    
    }//GEN-LAST:event_btnSaveActionPerformed

